package DateTime;

/**
 *
 * @author Thuan
 */
public class DateTimeError {
    private String dayFormatError;
    private String dayRangeError;
    private String monthFormatError;
    private String monthRangeError;
    private String yearFormatError;
    private String yearRangeError;
    private String invalidDate;
    private String validDate;

    public DateTimeError() {
    }

    public String getDayFormatError() {
        return dayFormatError;
    }

    public void setDayFormatError(String dayFormatError) {
        this.dayFormatError = dayFormatError;
    }

    public String getDayRangeError() {
        return dayRangeError;
    }

    public void setDayRangeError(String dayRangeError) {
        this.dayRangeError = dayRangeError;
    }

    public String getMonthFormatError() {
        return monthFormatError;
    }

    public void setMonthFormatError(String monthFormatError) {
        this.monthFormatError = monthFormatError;
    }

    public String getMonthRangeError() {
        return monthRangeError;
    }

    public void setMonthRangeError(String monthRangeError) {
        this.monthRangeError = monthRangeError;
    }

    public String getYearFormatError() {
        return yearFormatError;
    }

    public void setYearFormatError(String yearFormatError) {
        this.yearFormatError = yearFormatError;
    }

    public String getYearRangeError() {
        return yearRangeError;
    }

    public void setYearRangeError(String yearRangeError) {
        this.yearRangeError = yearRangeError;
    }

    public String getInvalidDate() {
        return invalidDate;
    }

    public void setInvalidDate(String invalidDate) {
        this.invalidDate = invalidDate;
    }

    public String getValidDate() {
        return validDate;
    }

    public void setValidDate(String validDate) {
        this.validDate = validDate;
    }
    
    
}
